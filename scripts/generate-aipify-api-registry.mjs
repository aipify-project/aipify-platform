#!/usr/bin/env node
/**
 * Scans canonical /api/aipify/{slug}/{dashboard|card|actions} routes and emits
 * lib/aipify/api-route-registry.generated.ts for consolidated dynamic handlers.
 *
 * Run: node scripts/generate-aipify-api-registry.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const API_ROOT = path.join(ROOT, "app/api/aipify");
const OUT = path.join(ROOT, "lib/aipify/api-route-registry.generated.ts");

function walk(dir, acc = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, acc);
    else if (entry.name === "route.ts") acc.push(full);
  }
  return acc;
}

function extractStringLiteral(src, pattern) {
  const m = src.match(pattern);
  return m?.[1] ?? null;
}

function isCanonicalDashboard(src) {
  return (
    src.includes("export async function GET") &&
    !src.includes("export async function POST") &&
    /supabase\.rpc\(/.test(src) &&
    /from "@\/lib\/aipify\//.test(src) &&
    !/from "@\/lib\/core\//.test(src)
  );
}

function isCanonicalCard(src) {
  return isCanonicalDashboard(src);
}

function isCanonicalActions(src) {
  return (
    src.includes("export async function POST") &&
    !src.includes("export async function GET") &&
    /supabase\.rpc\(/.test(src) &&
    !/from "@\/lib\/aipify\//.test(src)
  );
}

function parseDashboardOrCard(src) {
  const importMatch = src.match(
    /import\s*\{\s*([^}]+)\s*\}\s*from\s*"@\/lib\/aipify\/([^"]+)"/
  );
  const parseExport = importMatch?.[1]?.trim().split(",")[0]?.trim() ?? null;
  const lib = importMatch?.[2] ?? null;
  const rpc = extractStringLiteral(src, /supabase\.rpc\(\s*"([^"]+)"/);
  const errorMessage =
    extractStringLiteral(src, /return NextResponse\.json\(\{ error: "([^"]+)" \}, \{ status: 500 \}\)/) ??
    "Request failed";
  return { lib, parseExport, rpc, errorMessage };
}

function parseActions(src) {
  const rpc = extractStringLiteral(src, /supabase\.rpc\(\s*"([^"]+)"/);
  const errorMessage =
    extractStringLiteral(src, /return NextResponse\.json\(\{ error: "([^"]+)" \}, \{ status: 500 \}\)/) ??
    "Request failed";
  return { rpc, errorMessage };
}

function relSlug(filePath, suffix) {
  return filePath
    .replace(`${API_ROOT}${path.sep}`, "")
    .replace(`${path.sep}route.ts`, "")
    .slice(0, -suffix.length)
    .split(path.sep)
    .join("/");
}

const registry = {};
const canonicalFiles = [];

for (const file of walk(API_ROOT)) {
  const src = fs.readFileSync(file, "utf8");
  let kind = null;
  let slug = null;

  if (file.endsWith(`${path.sep}dashboard${path.sep}route.ts`)) {
    kind = "dashboard";
    slug = relSlug(file, "/dashboard");
    if (!isCanonicalDashboard(src)) continue;
  } else if (file.endsWith(`${path.sep}card${path.sep}route.ts`)) {
    kind = "card";
    slug = relSlug(file, "/card");
    if (!isCanonicalCard(src)) continue;
  } else if (file.endsWith(`${path.sep}actions${path.sep}route.ts`)) {
    kind = "actions";
    slug = relSlug(file, "/actions");
    if (!isCanonicalActions(src)) continue;
  } else {
    continue;
  }

  registry[slug] ??= { slug };
  registry[slug][kind] = kind === "actions" ? parseActions(src) : parseDashboardOrCard(src);
  canonicalFiles.push(file);
}

const slugs = Object.keys(registry).sort();

function emitParserLoader(slug, endpoint, kind) {
  const { lib, parseExport } = endpoint;
  if (!lib || !parseExport) {
    throw new Error(`Missing parser for ${slug}/${kind}`);
  }
  return `  "${slug}": async () => {
    const mod = await import("@/lib/aipify/${lib}/parse");
    return mod.${parseExport} as (data: unknown) => unknown;
  }`;
}

const dashboardLoaders = [];
const cardLoaders = [];
const rpcEntries = [];

for (const slug of slugs) {
  const entry = registry[slug];
  const rpcBlock = [`  "${slug}": {`];
  if (entry.dashboard) {
    rpcBlock.push(`    dashboard: { rpc: "${entry.dashboard.rpc}", errorMessage: ${JSON.stringify(entry.dashboard.errorMessage)} },`);
    dashboardLoaders.push(emitParserLoader(slug, entry.dashboard, "dashboard"));
  }
  if (entry.card) {
    rpcBlock.push(`    card: { rpc: "${entry.card.rpc}", errorMessage: ${JSON.stringify(entry.card.errorMessage)} },`);
    cardLoaders.push(emitParserLoader(slug, entry.card, "card"));
  }
  if (entry.actions) {
    rpcBlock.push(`    actions: { rpc: "${entry.actions.rpc}", errorMessage: ${JSON.stringify(entry.actions.errorMessage)} },`);
  }
  rpcBlock.push("  },");
  rpcEntries.push(rpcBlock.join("\n"));
}

const output = `// AUTO-GENERATED by scripts/generate-aipify-api-registry.mjs — do not edit manually.
// Canonical engine dashboard/card/actions endpoints consolidated for build graph reduction.

export type AipifyApiRpcEndpoint = {
  rpc: string;
  errorMessage: string;
};

export type AipifyApiEngineRpcConfig = {
  dashboard?: AipifyApiRpcEndpoint;
  card?: AipifyApiRpcEndpoint;
  actions?: AipifyApiRpcEndpoint;
};

/** ${slugs.length} engine slugs · ${canonicalFiles.length} consolidated route files */
export const AIPIFY_API_ENGINE_RPC: Record<string, AipifyApiEngineRpcConfig> = {
${rpcEntries.join("\n")}
};

export const AIPIFY_API_DASHBOARD_PARSERS: Record<
  string,
  () => Promise<(data: unknown) => unknown>
> = {
${dashboardLoaders.join(",\n")}
};

export const AIPIFY_API_CARD_PARSERS: Record<
  string,
  () => Promise<(data: unknown) => unknown>
> = {
${cardLoaders.join(",\n")}
};

export const AIPIFY_API_CONSOLIDATED_ROUTE_FILES = ${JSON.stringify(
  canonicalFiles.map((f) => path.relative(ROOT, f).split(path.sep).join("/")).sort(),
  null,
  2
)} as const;
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, output);

console.log(`Wrote ${OUT}`);
console.log(`Engines: ${slugs.length}`);
console.log(`Consolidated route files: ${canonicalFiles.length}`);
console.log(`  dashboard parsers: ${dashboardLoaders.length}`);
console.log(`  card parsers: ${cardLoaders.length}`);
console.log(
  `  actions RPCs: ${slugs.filter((s) => registry[s].actions).length}`
);

if (process.argv.includes("--apply")) {
  let removed = 0;
  for (const file of canonicalFiles) {
    if (fs.existsSync(file)) {
      fs.unlinkSync(file);
      removed++;
    }
  }
  console.log(`Removed ${removed} consolidated route files (--apply)`);
}
