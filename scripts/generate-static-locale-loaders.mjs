/**
 * Generates lib/i18n/static-locale-loaders.ts with explicit per-locale import paths.
 * Avoids webpack context modules from `import(\`@/locales/${locale}/...\`)` that bundle
 * all locales at compile time and cause OOM on Vercel.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const outPath = path.join(root, "lib/i18n/static-locale-loaders.ts");

const LOCALES = ["en", "no", "sv", "da", "pl", "uk", "es"];

const ROOT_NAMESPACES = [
  "common",
  "landing",
  "dashboard",
  "assistant",
  "auth",
  "support",
  "settings",
  "install",
  "platform",
  "branding",
  "presence",
  "license",
  "hosts",
  "marketing",
  "superAdmin",
  "growthPartnerPortal",
  "growthPortal",
  "partnersPortal",
  "partnerPortal",
  "partnerAcademy",
  "partnerCommissions",
  "partnerSettlements",
  "partnerCompliance",
  "partnerMaterials",
  "partnerOpportunities",
  "partnerCommunications",
  "partnerAdvisor",
  "companionMemoryContext",
  "companionWorkspaceIntelligence",
  "companionDeviceEnvironment",
  "companionActionApproval",
  "desktopCompanion",
  "commandBar",
];

const CUSTOMER_APP_SPLITS = [
  "navigation",
  "companion",
  "workforce",
  "warehouse",
  "growthPartners",
  "marketplace",
  "industryPacks",
  "digitalEmployees",
  "commandCenter",
  "settings",
  "dashboard",
  "core",
];

function exists(relPath) {
  return fs.existsSync(path.join(root, relPath));
}

function loaderBlock(varName, importPath) {
  const lines = LOCALES.map((locale) => {
    const file = `locales/${locale}/${importPath}`;
    if (!exists(file)) {
      return `    ${locale}: async () => (await import("@/locales/en/${importPath}")).default,`;
    }
    return `    ${locale}: async () => (await import("@/locales/${locale}/${importPath}")).default,`;
  });
  return `const ${varName}: Record<Locale, LocaleLoader> = {\n${lines.join("\n")}\n};`;
}

const parts = [
  `/** AUTO-GENERATED by scripts/generate-static-locale-loaders.mjs — do not edit manually. */`,
  `import type { Locale } from "./config";`,
  `import type { CustomerAppSplitName } from "./customer-app-split-config";`,
  `import type { Dictionary } from "./translate";`,
  ``,
  `type LocaleLoader = () => Promise<Dictionary>;`,
  ``,
];

for (const ns of ROOT_NAMESPACES) {
  const safe = ns.replace(/[^a-zA-Z0-9]/g, "_");
  parts.push(loaderBlock(`ROOT_${safe}`, `${ns}.json`));
  parts.push("");
}

parts.push(`export const ROOT_NAMESPACE_LOADERS: Record<string, Record<Locale, LocaleLoader>> = {`);
for (const ns of ROOT_NAMESPACES) {
  const safe = ns.replace(/[^a-zA-Z0-9]/g, "_");
  parts.push(`  ${JSON.stringify(ns)}: ROOT_${safe},`);
}
parts.push(`};`);
parts.push("");

for (const split of CUSTOMER_APP_SPLITS) {
  const safe = split.replace(/[^a-zA-Z0-9]/g, "_");
  parts.push(loaderBlock(`SPLIT_${safe}`, `customer-app/${split}.json`));
  parts.push("");
}

parts.push(
  `export const CUSTOMER_APP_SPLIT_LOADERS: Record<CustomerAppSplitName, Record<Locale, LocaleLoader>> = {`
);
for (const split of CUSTOMER_APP_SPLITS) {
  const safe = split.replace(/[^a-zA-Z0-9]/g, "_");
  parts.push(`  ${split}: SPLIT_${safe},`);
}
parts.push(`};`);
parts.push("");

fs.writeFileSync(outPath, `${parts.join("\n")}\n`);
console.log(`Wrote ${outPath}`);
