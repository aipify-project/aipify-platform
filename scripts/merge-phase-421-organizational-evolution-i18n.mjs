for (const locale of LOCALES) {
  const file = path.join(localesDir, locale, "customerApp.json");
  if (!fs.existsSync(file)) continue;
  const data = JSON.parse(fs.readFileSync(file, "utf8"));
  data.organizationalEvolutionEngine = BLOCK[locale];
  if (data.nav && typeof data.nav === "object") {
    data.nav.evolution = NAV_LABELS[locale];
  }
  fs.writeFileSync(file, `${JSON.stringify(data, null, 2)}\n`);
  console.log(`Updated ${locale}/customerApp.json`);
}
