const NAV_LABELS = {
  en: "Platform Excellence",
  no: "Plattformeksellanse",
  sv: "Plattformsexcellens",
  da: "Platform Excellence",
  pl: "Doskonałość platformy",
  uk: "Досконалість платформи",
};

for (const locale of LOCALES) {
  const filePath = path.join(localesDir, locale, "customerApp.json");
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  data.platformExcellenceEngine = BLOCK[locale];
  if (!data.nav) data.nav = {};
  data.nav.platformExcellenceEngine = NAV_LABELS[locale];
  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`);
  console.log(`Updated ${locale}/customerApp.json`);
}

